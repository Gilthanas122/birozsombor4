public class TwoNumbers {
    public static void main(String[] args) {
        int a = 22;
        int b = 13;
        System.out.println("a = " + a);
        System.out.println("b = " + b);


        System.out.println("a + b = " + (a + b));
        System.out.println("a - b = " + (a - b));
        System.out.println("a * b = " + (a * b));
        System.out.println("a / b (as a float) = " + ((float) a / b));
        System.out.println("a / b = " + (a / b));
        System.out.println("a % b = " + (a % b));
    }
}
