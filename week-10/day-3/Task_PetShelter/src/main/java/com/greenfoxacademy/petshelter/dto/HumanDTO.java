package com.greenfoxacademy.petshelter.dto;

public class HumanDTO {

  private String name;
  private Integer age;

  public HumanDTO() {
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public Integer getAge() {
    return age;
  }

  public void setAge(Integer age) {
    this.age = age;
  }
}
